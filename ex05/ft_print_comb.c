#include <unistd.h>

void ft_print_comb(void)
{
    char ans[5];

    ans[0] = '0';
    ans[3] = ',';
    ans[4] = ' ';
    while (ans[0] <= '6')
    {
        ans[1] = ans[0] + 1;
        while (ans[1] <= '8')
        {
            ans[2] = ans[1] + 1;
            while (ans[2] <= '9')
            {
                write(1, ans, 5);
                ans[2]++;
            }
            ans[1]++;
        }
        ans[0]++;
    }
    write(1, "789", 3);
}
